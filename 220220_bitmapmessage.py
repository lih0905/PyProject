print("비트맵에 띄울 문자열을 입력하세요.")
message = input('> ')
if message == '':
    sys.exit()

# 루프를 돌며 BITMAP의 각 행을 반복
for line in BITMAP.splitlines():
    # 루프를 돌며 행의 각 문자를 반복
    for i, bit in enumerate(line):
        if bit == ' ':
            # 공백이면 공백
            print(' ', end='')
        else:
            # message의 문자를 출력
            print(message[i % len(message)], end='')
